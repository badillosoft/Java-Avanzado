    private void btn_conectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_conectarActionPerformed
        try {
            usuario = new UsuarioImp(txt_nombre.getText(), txt_consola);
        } catch (RemoteException e) {
            System.out.println(e.getMessage());
            usuario = null;
            return;
        }

        try {
            chat = (Chat)Naming.lookup("chat");
        } catch (NotBoundException | MalformedURLException | RemoteException e) {
            System.out.println(e.getMessage());
            chat = null;
            return;
        }
        
        try {
            if (!chat.conectar(usuario)) {
                txt_consola.append("\r\nEl usuario ya existe");
                return;
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            return;
        }

        txt_nombre.setEnabled(false);
        btn_conectar.setEnabled(false);
        txt_mensaje.setEnabled(true);
        btn_enviar.setEnabled(true);
    }//GEN-LAST:event_btn_conectarActionPerformed

    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        if (usuario == null || chat == null) {
            txt_consola.append("\r\nHubo un error inesperado");
            return;
        }
        
        try {
            chat.enviarMensaje(usuario, txt_mensaje.getText());
        } catch(Exception e) {
            txt_consola.append("\r\nEl mensaje no se pudo enviar");
            return;
        }
        
        txt_mensaje.setText("");
    }//GEN-LAST:event_btn_enviarActionPerformed

